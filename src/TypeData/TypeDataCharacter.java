package TypeData;

public class TypeDataCharacter {
    public static void main(String[] args) {
        char a = 'a';
        char b = 'b';
        char c = 'c';

        // garis baru
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        // tidak membuat garis baru
        System.out.print(a);
        System.out.print(b);
        System.out.print(c);
    }
}
